    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        cnx.Conecta();
        try{
            String SQL ="update escuela set nombreE=?, idfacultad=?"
            + "where idescuela=?";
            int fila = tblEscuela.getSelectedRow();
            String dato = (String)tblEscuela.getValueAt(fila, 0);
            PreparedStatement ps = cnx.conn.prepareStatement(SQL);
            ps.setString(1, txtEscuela.getText());
            ps.setInt(2, f.consultaId((String)cbxFacultad.getSelectedItem()));
            ps.setString(3, dato);

            int n = ps.executeUpdate();
            if(n>0){
                JOptionPane.showMessageDialog(null, "Datos actualizados correctamente");
                LlenarTabla();
            }
        }catch(SQLException | HeadlessException e){
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
        cnx.Desconecta();
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tblEscuela.getSelectedRow();
        cnx.Conecta();
        try {
            String SQL = "delete from escuela where idescuela= " + tblEscuela.getValueAt(fila, 0);
            PreparedStatement ps = cnx.conn.prepareStatement(SQL);            
            int n = ps.executeUpdate(SQL);
            if(n>0){
                limpiar();
                LlenarTabla();
                JOptionPane.showMessageDialog(null, "Datos eliminados correctamente");
            }
        } catch(SQLException | HeadlessException e){
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
        cnx.Desconecta();
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        cnx.Conecta();
        try {
            String SQL = "insert into escuela(nombreE,idfacultad) "
            + "values(?,?)";
            PreparedStatement ps = cnx.conn.prepareStatement(SQL);
            ps.setString(1, txtEscuela.getText());
            ps.setInt(2, f.consultaId(cbxFacultad.getSelectedItem().toString()));
            int n = ps.executeUpdate();
            if (n>0){
                JOptionPane.showMessageDialog(null, "Datos guardados correctamente");
                LlenarTabla();
            }
        } catch(SQLException | HeadlessException e){
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
        cnx.Desconecta();
    }//GEN-LAST:event_btnGuardarActionPerformed

